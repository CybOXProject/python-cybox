        if pretty_print:
            eol_ = '\n'
        else:
            eol_ = ''
        if self.Port_Value is not None:
            self.Port_Value.export(lwrite, level, 'PortObj:', name_='Port_Value', pretty_print=pretty_print)
        if self.Layer4_Protocol is not None:
            self.Layer4_Protocol.export(lwrite, level, 'PortObj:', name_='Layer4_Protocol', pretty_print=pretty_print)
