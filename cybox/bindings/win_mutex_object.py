class WindowsMutexObjectType(mutex_object.MutexObjectType):
    """The WindowsMutexObjectType type is intended to characterize Windows
    mutual exclusion (mutex) objects."""
    
    subclass = None
    superclass = mutex_object.MutexObjectType
    def __init__(self, object_reference=None, Custom_Properties=None, xsi_type=None, named=None, Name=None, Handle=None, Security_Attributes=None):
        super(WindowsMutexObjectType, self).__init__(object_reference, Custom_Properties, named, Name, )
        self.Handle = Handle
        self.Security_Attributes = Security_Attributes
    def factory(*args_, **kwargs_):
        if WindowsMutexObjectType.subclass:
            return WindowsMutexObjectType.subclass(*args_, **kwargs_)
        else:
            return WindowsMutexObjectType(*args_, **kwargs_)
    factory = staticmethod(factory)
    def get_Handle(self): return self.Handle
    def set_Handle(self, Handle): self.Handle = Handle
    def get_Security_Attributes(self): return self.Security_Attributes
    def set_Security_Attributes(self, Security_Attributes): self.Security_Attributes = Security_Attributes
    def validate_StringObjectPropertyType(self, value):
        # Validate type cybox_common.StringObjectPropertyType, a restriction on None.
        pass
    def hasContent_(self):
        if (
            self.Handle is not None or
            self.Security_Attributes is not None or
            super(WindowsMutexObjectType, self).hasContent_()
            ):
            return True
        else:
            return False
    def export(self, lwrite, level, namespace_='WinMutexObj:', name_='WindowsMutexObjectType', namespacedef_='', pretty_print=True):
        if pretty_print:
            eol_ = '\n'
        else:
            eol_ = ''
        showIndent(lwrite, level, pretty_print)
        lwrite('<%s%s%s' % (namespace_, name_, namespacedef_ and ' ' + namespacedef_ or '', ))
        already_processed = set()
        self.exportAttributes(lwrite, level, already_processed, namespace_, name_='WindowsMutexObjectType')
        if self.hasContent_():
            lwrite('>%s' % (eol_, ))
            self.exportChildren(lwrite, level + 1, namespace_, name_, pretty_print=pretty_print)
            showIndent(lwrite, level, pretty_print)
            lwrite('</%s%s>%s' % (namespace_, name_, eol_))
        else:
            lwrite('/>%s' % (eol_, ))
    def exportAttributes(self, lwrite, level, already_processed, namespace_='WinMutexObj:', name_='WindowsMutexObjectType'):
        super(WindowsMutexObjectType, self).exportAttributes(lwrite, level, already_processed, namespace_, name_='WindowsMutexObjectType')
    def exportChildren(self, lwrite, level, namespace_='WinMutexObj:', name_='WindowsMutexObjectType', fromsubclass_=False, pretty_print=True):
        super(WindowsMutexObjectType, self).exportChildren(lwrite, level, 'WinMutexObj:', name_, True, pretty_print=pretty_print)
        if pretty_print:
            eol_ = '\n'
        else:
            eol_ = ''
        if self.Handle is not None:
            self.Handle.export(lwrite, level, 'WinMutexObj:', name_='Handle', pretty_print=pretty_print)
        if self.Security_Attributes is not None:
            self.Security_Attributes.export(lwrite, level, 'WinMutexObj:', name_='Security_Attributes', pretty_print=pretty_print)
    def build(self, node):
        already_processed = set()
        self.buildAttributes(node, node.attrib, already_processed)
        for child in node:
            nodeName_ = Tag_pattern_.match(child.tag).groups()[-1]
            self.buildChildren(child, node, nodeName_)
    def buildAttributes(self, node, attrs, already_processed):
        super(WindowsMutexObjectType, self).buildAttributes(node, attrs, already_processed)
    def buildChildren(self, child_, node, nodeName_, fromsubclass_=False):
        if nodeName_ == 'Handle':
            obj_ = win_handle_object.WindowsHandleObjectType.factory()
            obj_.build(child_)
            self.set_Handle(obj_)
        elif nodeName_ == 'Security_Attributes':
            obj_ = cybox_common.StringObjectPropertyType.factory()
            obj_.build(child_)
            self.set_Security_Attributes(obj_)
        super(WindowsMutexObjectType, self).buildChildren(child_, node, nodeName_, True)
# end class WindowsMutexObjectType

